package solvers

import (
	"testing"
)

func TestSolverThree_SolveFirstProblem(t *testing.T) {
	solver := &SolverThree{}
	solver.content = [][]string{
		{"4", "6", "7", ".", ".", "1", "1", "4", ".", "."},
		{".", ".", ".", "*", ".", ".", ".", ".", ".", "."},
		{".", ".", "3", "5", ".", ".", "6", "3", "3", "."},
		{".", ".", ".", ".", ".", ".", "#", ".", ".", "."},
		{"6", "1", "7", "*", ".", ".", ".", ".", ".", "."},
		{".", ".", ".", ".", ".", "+", ".", "5", "8", "."},
		{".", ".", "5", "9", "2", ".", ".", ".", ".", "."},
		{".", ".", ".", ".", ".", ".", "7", "5", "5", "."},
		{".", ".", ".", "$", ".", "*", ".", ".", ".", "."},
		{".", "6", "6", "4", ".", "5", "9", "8", ".", "."},
	}
	firstProblemAns := solver.SolveFirstProblem()
	if firstProblemAns != 4361 {
		t.Errorf("Expected 4361, got %d", firstProblemAns)
	}
}
